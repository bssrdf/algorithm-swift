import UIKit
//https://leetcode-cn.com/problems/spiral-matrix-ii/
//si lu:按数字递增，依次填入二维数组


class Solution {
    func generateMatrix(_ n: Int) -> [[Int]] {
        var arrYiWei = Array.init(repeating: 0, count: n)
        var arr = Array.init(repeating: arrYiWei, count: n)
        
        return arr
        
    }
}
